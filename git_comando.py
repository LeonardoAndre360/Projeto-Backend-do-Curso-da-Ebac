# git init: Inicializar um novo repositório Git
# git clone <url>: Clonar um repositório remoto para o computador local
# git status: Mostre o estado atual do repositório
# git add <file>: Adicione um arquivo à área de preparação.
# git add .: Adicione todos os arquivos alterados à área de preparação.
# git commit -m "mgs": Confirme as alterações em etapas com uma mensagem.
# git log: Mostrar o histórico de commits
# git log --oneline: Exibir um histórico de commits conciso
# git diff: Mostre a diferença entre as mudanças
# git diff <file>: Mostrar alterações para um arquivo específico

# git branch: Listar todas as branches
# git branch <branch-name>: Criar um novo branch
# git checkout <branch-name>: Mudar o branch
# git checkout -b <name>: Criar e mudar para um novo branch
# git merge <branch-name>: Mesclar um branch no branch atual
# git branch -d <name>: Deletar o branch
# git branch -D <name>: Forçar o delete da branch 